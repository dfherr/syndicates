<b>Syndicates – Quickstart</b>
<br><br><br>
<b>Die Schutzzeit:</b>
<br><br>
Erste Schritte werden im spielinternen Tutorial erläutert.
Dieser Quickstart Guide soll dazu dienen, Hilfestellung für die ersten Spieltage zu geben.
Da Sie in Syndicates mit vielen anderen Spielern interagieren können, sollten Sie zuerst sicherstellen, dass Ihr Konzern die nötigen Vorkehrungen trifft um in diesem Umfeld nicht zum wehrlosen Opfer zu werden.

<br><br><br>
<b>Die Einheiten:</b>
<br><br>
Bauen Sie zu Anfang keine Offensiveinheiten! Je mehr Offensive Sie haben, desto weniger Defensive können Sie sich leisten und damit steigt die Wahrscheinlichkeit, von anderen Spielern angegriffen zu werden. Begnügen Sie sich vorerst mit friedlichem Wachstum und kaufen Sie neues Land direkt.
Bis 2000ha bieten Ranger noch preiswerten Schutz (vor allem mit den frühen Forschungen Ranger Upgrade und Ranger & Marine Training); danach sollte man allerdings schnellstmöglich auf bessere Einheiten (Wartank/Stalker/Robotic Wall/Phönix) umsteigen.
<br><br>
Bei Spionaheeinheiten empfiehlt sich Spezialisierung. Wenn sie andere Spieler bestehlen wollen, setzen Sie auf Thiefs/V-Männer (je nach Fraktion).
Wenn Sie sich ersteinmal umschauen möchten und verhindern wollen, dass Sie bestholen werden, sollten Sie Guardians/Hacker (je nach Fraktion) bauen.
<br><br><br>
<b>Die Gebäude:</b>
<br><br>
Auch bei Gebäuden sollte selektiv vorgegangen werden. Es empfiehlt sich immer, eine konkrete Strategie zu verfolgen.
Ein bisschen von allem bringt rein gar nix. Im Folgenden hab ich einige sinnvolle Gebäudestrukturen vorgestellt. Allgemein gilt: niemals mehr als 3 verschiedene Gebäudetypen besitzen!
<br><br>
Unter 2000ha:
20% Fabriken 40% Syndikatshandelszentren 40% Syndikatserzförderungsanlagen (NEB/UIC) oder 40% Syndikatsforschungslabore (SL) (als BF nimmt man 80% Syndikatshandelszentren)
<br><br>
(um Syndikatsgebäude bauen zu können benötigt man die Forschung Advanced Building Construction) 

Zwischen 2000 und 4000ha gibt es im wesentlichen 3 Möglichkeiten:
<br><br>
<b>Selbstversorger</b>: 20% Fabriken 30% Lagerhallen 50% Produktion (EINE Ressource)
<br><br>
<b>Produzent</b>: 30% Lagerhallen 70% Produktion (mindestens 35% einer Ressource)
nur die beste Verteidigungseinheit bauen/kaufen (s.o.)
<br><br>
<b>Seller</b>: 20% Fabriken 80% Lagerhallen
von allem etwas bauen und am Global Market verkaufen; 12-15 der besten Verteidigungseinheiten pro Hektar bleiben als Verteidigung zu Hause
<br><br>
Der Selbstversorger lohnt sich zwischen 4000 und 7000ha nicht mehr unbedingt, daher:
<br><br>
<b>Produzent</b>:50% Lagerhallen 50% Produktion (EINE Ressource; keine Cr mehr)
oder
<b>Seller</b>:20% Fabriken 20% Forts 60% Lagerhallen
<br><br><br>
<b>Das Wachstum und der FOX:</b>
<br><br>
Es ist sehr riskant, mehr als 400 ha auf einmal zu kaufen. Lagerhallen (die natürlich auch mit Verteidigungseinheiten gefüllt sein müssen) sichern das Wachstum ab und erleichtern es damit ungemein.
Faustregel zum Expandieren: Immer nur so viele ha kaufen wie man Lagerhallen besitzt.
Niemals expandieren bevor nicht alles an Militär und Spionen voll ist.
<br><br>
Der FOX eines Konzerns berechnet sich, indem man die Konzernstärke (Networth [NW]) durch die Landgrösse teilt. Anhand des FOX´ suchen Angreifer und Spione ihre Opfer aus. Man sollte also immer darauf achten einen recht guten FOX zu haben.
<br><br>
Zur Orientierung einige „FOX Richtlinien“:
<br><br>
Unter 1000 ha: nicht von Bedeutung (sollte allerdings auch nicht schlechter als 60 sein)
bis 2000 ha: um die 100
2000-4000 ha: 120-150
ab 4000 ha: 150-250
(ab hier sollte man sich am relativen Ranking seiner Fraktion orientieren)
<br><br><br>
<b>Die Forschungen:</b>
<br><br>
Forschungen sind extrem wichtig! Man sollte pausenlos durchforschen und nur für im Notfall, d.h. wenn man keine Forschungspunkte auftreiben kann, gamblen. Eine Ausnahme ist hierbei die Schutzzeit, während der man z.b. Strategical Allocation 1 und 5x Gamble forschen kann.
<br><br>
Direkt nach der Schutzzeit sollte man dringendst Advanced Building Construction und Motivationsprogramm für deprimierte Wissenschaftler forschen. Danach erstmal Stufe 1 Economic Sciences (Pure Capitalism/Better Ore Mining).
<br><br>
Anfangs sind Ranger Upgrade und Ranger & Marine Training eine extreme Verbesserung der Ranger. Wer auf Nummer Sicher gehen möchte sollte diese Forschungen schnellstmöglich abschließen.
<br><br>
Wer den Rest der Runde sich aufs Produzieren spezialisieren will sollte jetzt Richtung Economic Domination (Industrial Zweig Stufe 5) forschen. Auf dem Weg dahin erforscht man Cheaper Unit Construction, das sich sowohl für den eigenen Unitbau wie auch für mögliche Verkäufe als wertvoll erweisen kann.
<br><br>
Danach sollte man den 36h Tag für übermotivierte Wissenschaftler einschieben und dann die Syndikatsforschungen Inner Syndicate Energy Saving Program, Inner Syndicate Trade Program und Inner Syndicate Spy Defense Network komplett durchforschen.
<br><br>
Dann kommt Better Space Management 1-3 an die Reihe sowie die Defensivforschungen.
<br><br>
Defensivforschungen sind ungemein wichtig. Defense Network und Orbital Defense System sind DIE zentralen Defensivforschungen und sollten sobald man über 4000ha wächst gemacht werden (spätestens!).
<br><br>
Weiterführende Strategien und Anregungen gibt es im Strategie Forum!
(einfach mal rumstöbern )
<br><br>
Die drei Grundstrategien
<br><br><br>
<b>Der Producer</b>
<br><br>
Als Producer finanziert man sich durch das produzieren und den Verkauf von Rohstoffen am Global Market. Militär kauft man am Global Market; man besitzt am besten ausschliesslich die beste Verteidigungsunit. Um genügend Verteidigung zu haben sollte man 20-30% Lagerhallen, neben seiner herkömmlichen Produktion besitzen. Im späteren Spielverlauf bleiben Lagerhallen für Producer die bessere Alternative (statt Forts).
Essentielle Forschungen für Producer sind Economic Domination, Stufe 1 Eco Forschungen und Defensive Forschungen. Ebenso sollte man mittels Orbital Defense System seine Landverluste verringern, da man als Producer von seiner Landgrösse lebt; folglich sollte ein Producer auch immer versuchen schnell zu wachsen (ACHTUNG: Trotzdem niemals die Verteidigung vernachlässigen!)
<br><br>
Dies ist die zeitschonendste Strategie in Syndicates. Zwei kurze logins morgens und abends reichen bereits aus um weit zu kommen.
<br><br>
Gebstruktur: 70%-80% Produktion (35%-40% Credits 35%-40% Fraktionstypisch) 20%-30% Lagerhallen
<br><br><br>
<b>Der Seller</b>
<br><br>
Als Seller verdient man sein Geld durch den Bau und Verkauf von Militär am GM. Mit Fabriken und der Forschung Cheaper Unit Construction baut man seine Einheiten für 40% des Herstellungspreises und verkauft diese dann für 55%-75% wieder am Markt.
Man sollte immer darauf achten von allen Einheiten etwas zu bauen um nicht so anfällig für Marktschwankungen zu sein (mal sind die Preise bei einer bestimmten Einheit besser, mal schlechter).
Ausserdem sollte man aufpassen, dass man nicht zu viel verkauft und immer eine ausreichende Verteidigung zu Hause lässt. (12-15 der besten Verteidungseinheiten pro Hektar)
Da ein Seller naturgemäß viele Lagerhallen besitzt, bieten sich Forts an um mehr sellen zu können.
<br><br>
Gebstruktur: 20% Fabriken (Cheaper Unit Construction Level 1) 80% Lagerhallen (alternativ: 60% Lagerhallen 20% Forts)
<br><br>
Wichtige Forschungen: Cheaper Unit Construction Level 3 (bei Level 3 werden nur noch 12% Fabriken benötigt), Better Space Management 3 (Mehr Platz bedeutet mehr units zum sellen, was wiederum mehr Gewinn bedeutet), alle defensivforschungen
<br><br>
Als Level 5 Forschung bietet sich Relentless Assault an (falls Ihr euch später evt überlegt anzugreifen, was als Seller aufgrund der vielen Lagerhallen ganz gut möglich ist)
