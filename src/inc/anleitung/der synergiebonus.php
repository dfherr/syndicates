Die meisten produzierenden Gebäude sind von dem sogenannten "Synergiebonus" betroffen.<br>
Der Synergiebonus soll die Spezialisierung unter den Spielern eines Syndikates und damit das Teamplay fördern.<br><br>
Der Synergiebonus wirkt sich folgendermaßen aus: Je mehr Gebäude eines Typs ich besitze (prozentual), desto effizienter arbeiten diese.<br><br>
Die genaue Formel lautet: <br><br><b>Prozentualer Gebäudeanteil * Prozentualer Gebäudeanteil * 5 = Synergiebonus für alle Gebäude eines Typs</b><br><br>
<i>Zur Veranschaulichung ein kleines Beispiel:</i><br>
Ich besitze 100 Land,
15 Handelszentren (15%) und 25 Kraftwerke (25%).<br>
Jedes Handelszentrum produziere 100 Credits, jedes Kraftwerk produziere 100 Energie (Achtung, diese Werte stimmen nicht mit den echten überein, aber es lässt sich wesentlich einfacher damit rechnen!).<br>
Wer dies nachrechnet, wird aber feststellen, dass die Produktion eines Handelszentrums ca. 111,25 Credits beträgt (etwa 11% mehr als angegeben) und die Produktion eines Kraftwerkes sogar 131,25 Energieeinheiten (31% Bonus).<br>
Der maximale Synergiebonus wird bei 35% eines Gebäudetyps erreicht und liegt bei ca. 61,25%.<br>
Gebäude eines Typs sind Gebäude, welche die gleiche Ressource produzieren.
