Spieler, die sich zu lange nicht einloggen, werden inaktiv.<br>
Syndicates unterscheides verschiedene Inaktivitätsstufen:<br>
<ul>
<li>Lokale Inaktivität - ein Spieler, der sich 2,5 Tage (Basic-Server: 7 Tage) lang nicht eingelogt hat, wird lokal inaktiv. Er kann jetzt von Spielern aus dem eigenen Syndikat angegriffen und bestohlen werden und erhält dabei keinerlei Schutz vor Mehrfachangriffen.
<li>Globale Inaktivität - ein Spieler, der sich 4 Tage (Basic-Server: 14 Tage) lang nicht eingelogt hat, wird global inaktiv. Er kann jetzt von jedem Spieler angegriffen oder bestohlen werden und erhält dabei keinerlei Schutz vor Mehrfachangriffen, außerdem produzieren global inaktive Spieler keine Ressourcen mehr.
<li>Gelöscht - der Konzern eines Spielers, bei dem der letzte Login 5,5 Tage (Basic-Server: 21 Tage) oder mehr zurückliegt, wird automatisch gelöscht. (Spieler mit Bezahlaccount werden nicht automatisch gelöscht!).
</ul>

