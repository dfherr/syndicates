Irgendwann passiert es jedem einmal - der erste Angriff kam schnell, überraschend unerwartet und vernichtend.

Natürlich möchten Sie sich für diesen hinterhältigen Angriff bei Ihrem Gegner rächen und am liebsten sofort zurückschlagen.

Bei Syndicates gibt es hierfür das sogenannte "Racherecht" (manchmal auch Rückschlagrecht genannt).
Hierbei ist folgendes zu beachten:
<br><br>
<ol>
<li>Folgende Faktoren/Auslöser gewähren Ihnen gegenüber einem anderen Konzern Racherecht
	<ul>
		<li>Der Konzern hat innerhalb der letzten 24h einen erfolgreichen Angriff gegen Sie ausgeführt
		<li>Der Konzern hat innerhalb der letzten 24h die Spionageaktion "Forschung zerstören" gegen Sie erfolgreich ausgeführt
		<li>Der Konzern hat innerhalb der letzten 24h mindestens <? echo RACHERECHT_ON_SPYACTIONS_NUMBER ?> erfolgreiche Sabotageaktionen gegen Sie ausgeführt (genaueres hierzu siehe in der <a href=index.php?action=docu&kat=1&aid=6 class=gelblink>Hilfe für Spionage</a>)
	</ul>
	<br>
<li>Bei Racherecht gelten folgende Vergünstigungen bei Angriffen / Spionageaktionen
	<ul>
		<li>Etwaiger Schutz vor Angriffen/Sabotage durch Aktien wird ausgesetzt (siehe <a href=index.php?action=docu&kat=2&aid=8 class=gelblink>Börse</a>).
		<li>Sie dürfen den Spieler, bei dem Sie Racherecht haben beliebig oft angreifen. Die Angriffe zählen nicht zu den (derzeit 5) maximal möglichen Angriffen pro Tag hinzu.
		<li>Der Schutz vor Mehrfachangriffen wird wie im Krieg gehandhabt, ist also deutlich reduziert.
		<li>Bezüglich Spionage gelten die gleichen Vergünstigungen, als befänden sich Ihre Syndikate im Krieg miteinander.
	</ul>
</ol>

