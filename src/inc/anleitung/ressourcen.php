<b><i>Credits:</i></b>
Credits sind die monetäre Währung in der Welt von Syndicates, gleichzeitig ist der Credit die flexibelste Ressource des Spiels. Um Credits zu produzieren, sollten Sie die Gebäude <a href="index.php?action=docu&kat=1&aid=3" class="gelblink">Handelszentren</a> oder <a href="index.php?action=docu&kat=1&aid=3" class="gelblink">Diamentenminen</a> errichten.<br>
Sie benötigen Credits, um:<br>
<ul>
<li>Gebäude zu bauen
<li>Land zu kaufen
<li>Militär und Spionageeinheiten auszubilden
<li>Ressourcen auf dem <a href="index.php?action=docu&kat=2&aid=7" class="gelblink">Global Market</a> zu erstehen
<li>Aktien von anderen Syndikaten zu erwerben
</ul> 
<br><br>
<b><i>Energie</i></b>:
Energie wird von <a href="index.php?action=docu&kat=1&aid=3" class="gelblink">Kraftwerken</a> produziert und in MWh gemessen. Sie benötigen Energie um Spionage- und Militäreieinheiten zu produzieren, außerdem sollten Sie beachten, dass zum reibungslosen Betrieb ihrer Gebäude genügend Energie vorhanden ist.
<br><br>
<b><i>Erz:</i></b>
Erz wird in <a href="index.php?action=docu&kat=1&aid=3" class="gelblink">Erzförderungsanlagen</a> produziert und in Tonnen (t) gemessen.<br>
Erz wird ausschließlich zur Produktion von Militäreinheiten benötigt.<br><br>
<b><i>Forschungspunkte</i></b>:
Forschungspunkte werden von <a href="index.php?action=docu&kat=1&aid=3" class="gelblink">Forschungslaboren</a> bereitgestellt und mit dem Kürzel P abgekürzt.<br>
Forschungspunkte werden ausschließlich zum Entwickeln neuer Forschungen und zum Bau hochspezialisierten Militärs benötigt.
<br><br>
Zu Anfang stellen folgende Werte ein faires Tauschverhältnis dar. Dies kann sich im späteren Rundenverlauf ändern, jenachdem wie sich Angebot und Nachfrage entwickeln.
