<a name="schritte"></a>
<b><u>Im Spiel</u></b>
<p>
Nach Ihrem ersten Login begrüßt sie die Statusseite. Sie bietet einen umfassenden Überblick über den Zustand Ihres Konzerns. Das Menu auf der linken Seite des Bildschirms mag anfangs etwas verwirrend aussehen, daher widmen wir den einzelnen Menupunkten kurz einige Wort:
<ol>
	<li> <b>Konzern:</b><br><br>
		<ul>
			<li> <b>Status</b> - Übersicht über den Zustand des Konzerns
			<li> <b>Bauaufträge</b> - Detailinformationen zu laufenden Bauaufträgen, Möglichkeit diese abzubrechen
			<li> <b>Gebäude und Land</b> - Hier können neue Gebäude errichtet oder weiteres Land dazugekauft werden
			<li> <b>Militär</b> - Rekrutierung neuer Militär- oder Spionageeinheiten
			<li> <b>Forschung</b> - Entwicklung von Forschungen
			<li> <b>Angriff</b> - Kampfhandlungen gegen einen anderen Spieler unternehmen
			<li> <b>Spionage</b> - Spionage- oder Sabotageaktionen gegen einen anderen Spieler unternehmen
			<li> <b>myKonzern </b> - Geben sie ihrem Konzern eine individuelle Note!
		</ul>
	<br><br>
	<li> <b>Kommunikation</b><br><br>
		<ul>
			<li> <b>Mitteilungen</b> - Mitteilungen anderer Spieler an Sie
			<li> <b>Nachrichten</b> - Benachrichtigungen über Ereignisse, die Ihren Konzern betreffen
			<li> <b>Synd. Board</b> - Das Syndikatsforum, die Kommunikationsplattform Ihres Syndikates
			<li> <b>Allg. Board</b> - Das allgemeine Forum, hier finden sich viele nützliche Informationen zum Spiel
		</ul>
	<br><br>
	<li> <b>Syndikat und Welt:</b><br><br>
		<ul>
			<li> <b>Syndikatsübers.</b> - Übersicht über die einzelnen Konzerne in einem Syndikat
			<li> <b>Global Market</b> - Kaufen sie Ressourcen, Militär- oder Spionageeinheiten von anderen Spielern
			<li> <b>Lager & Transfer</b> - Interner Ressourcenhandel mit Mitgliedern Ihres Syndikats
			<li> <b>Börse</b> - Der Aktienmarkt von Syndicates
			<li> <b>Politik</b> - Syndikatspolitik: aktuelle Kriege, Allianzen und die Wahl der Präsidenten
			<li> <b>Aktuelles</b> - Eine Übersicht über die wichtigsten Ereignisse, die Ihr Syndikat betreffen
			<li> <b>Rankings</b> - Die besten Spieler von Syndicates
		</ul>
</ol>
