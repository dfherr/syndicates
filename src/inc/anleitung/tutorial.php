<a name="einleitung"></a>
<b><u>Einleitung</u></b>
<p>
Syndicates ist ein sogenanntes Browserspiel. Sie können ohne Installation eines Programmes bequem über ihren Webbrowser (z.B. Internt Explorer) spielen.
Obwohl Syndicates sehr viel Spieltiefe bieten kann, ist das Spiel recht einfach zu erlernen.<br>
Eine minimalistische Beschreibung des Spiels k&ouml;nnte so aussehen:<br>
Jeder Spieler spielt einen Konzern.<br>
Dieser Konzern setzt sich zusammen aus:<br>
<ol type="1">
<li>Einigen Hektar Land
<li>Geb&auml;uden (ganz einfach: pro Hektar Land kann ein Geb&auml;ude gebaut werden)
<li>Milit&auml;reinheiten
<li>Spionageeinheiten
<li>evtl einigen Forschungen
</ol><br><br>
Der Reiz des Spieles besteht in der Interaktion mit den mehreren hundert anderen Spielern.
Es ist m&ouml;glich andere Spieler anzugreifen oder diverse Spionageaktionen gegen sie zu ver&uuml;ben, Allianzen zu bilden und Kriege zu führen.
<br><br>

<u>Der Spielablauf:</u><br><br>
Eine Stunde Realzeit entspricht einem Zug in Syndicates. Jede Stunde bekommen Sie Ressourcen, mit deren Hilfe Sie neue Gebäude bauen, neues Land erwerben, Milit&auml;reinheiten in Auftrag geben oder Forschungen entwickeln können. Der Bau von Geb&auml;uden oder Einheiten dauert beispielsweise 20 Stunden, wenn sie also neue Geb&auml;ude bauen, sind diese nach 20 Stunden verf&uuml;gbar.<br>
Eine Spielrunde dauert f&uuml;r gew&ouml;hnlich sieben Wochen, danach gibt es einen Reset und jeder Spieler f&auml;ngt wieder bei null an, um Spielern, die erst seit kurzem dabei sind gleiche Chancen einzur&auml;umen.<br>
<br><br>

<u>Hintergrund:</u><br>
<br>
In einer vom Frieden und Wohlstand verw&ouml;hnten Welt, entsteht ein neues System. Die großen Multikonzerne reißen die Macht an sich. Die Politiker sind nur noch hohle Marionetten die von den <b>Konzernen</b> bezahlt und gelenkt werden.<br>
Der Spieler &uuml;bernimmt die Kontrolle &uuml;ber einen Konzern. Bis zu <b><? echo MAX_USERS_A_SYNDICATE; ?> Konzerne</b> schließen sich zu einem <b>Syndikat</b> zusammen. Spieler eines Syndikates spielen zusammen und bilden eine Art Team, dazu gibt es viele Möglichkeiten, wie Ressourcentransfers oder Gemeinschaftsforschungen.<br>
In jedem Syndikat wird ein Spieler zum <b>Pr&auml;sident</b> gew&auml;hlt, der sich um verschiedene politische Aufgaben k&uuml;mmert.<br><br><br>


