<a name="final"></a>
<b><u>Finale</u></b>
<p>
 Wie geht es jetzt weiter ?<br><br>
Alle neuen Konzerne befinden sich zu Anfang <b>zwei Tage</b> lang unter <b>Schutz</b>. In dieser Zeit sind Sie vor den Aktionen fremder Konzerne sicher. Folgen sie den Anweisungen des ingame-Tutorials und versuchen sie nach und nach einen Überblick über alle Möglichkeiten zu gewinnen.<br>
Setzen sie sich am besten mit Ihren Syndikatsmitgliedern in Verbindung (Menupunkt Synd. Board), diese können Ihnen meistens viele gute Tips geben.
