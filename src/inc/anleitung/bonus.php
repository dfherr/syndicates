Werden auf einen Wert mehrere Boni verrechnet, werden diese zuerst aufsummiert und anschließend mit dem zu modifizierenden Wert multipliziert.<br><br>
<i>Beispiel:</i><br>
Wir möchten einen Spieler mit einem Marine angreifen.
Wir haben "Basic Offense Tactics" auf Stufe1 erforscht, ausserdem Spielen wir die Fraktion "Brute Force".<br>
Unser Marine hat 12 Angriffspunkte.<br>
Wir bekommen 10% Angriffsbonus (Fraktionsbonus)<br>
und 5% Angriffsbonus durch unsere Forschung.<br>
Unser Marine besitzt folglich: <i>12 * (1 + 0,10 + 0,05) = 12 * 1,15 = 13,8</i> Angriffspunkte.

