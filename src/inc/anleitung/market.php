Militär- bzw. Spionageeinheiten die auf dem Global Market erworben werden sind bereits nach 10 Stunden verfügbar.<br>

Beim Kauf von Einheiten wird neben dem verlangten Preis, der umgerechnete Wert (in Cr) der Einheiten angezeigt.
 Mindestpreis für Militäreinheiten und Spione
sind 30% des durchschnittlichen Preises der letzten 6 Stunden umgerechnet in Credits.<br>
Für Ressourcen existieren keine Preisbeschränkungen.<br><br><em> (Achtung: der Preis für Ressourcen bezieht sich auf jeweils 10 Einheiten, der Preis für Militäreinheiten
und Spione auf einzelne Einheiten. Dies ermöglicht eine feinere Einteilung beim Angebot von Ressourcen.<br>


<br><br>
<b>Der Marktkaufassistent</b>
<br>
Mit Hilfe des Marktkaufassistents können bis zu 5 Kaufgebote abgegeben werden.
Sobald ein Produkt höchstens zu dem gebotenen Preis verkauft wird, wird es automatisch vom Marktkaufassistenten erworben. Gezahlt wird dabei immer der beim Kaufgebot angegebene Preis.


Achtung: Ressourcen, die auf dem Global Market angeboten werden können weiterhin gestohlen werden, Militär- oder Spionageeinheiten, die noch nicht verkauft sind, verteidigen weiterhin und können entsprechend sterben oder sabotiert werden!)
<br>
</em>
<br>
Es kann passieren, dass der Preis zwischen Anzeige der Marktseite und Kaufanfrage sinkt, in diesem Fall wird zu dem günstigeren Preis eingekauft. Steigt der Preis, wird die Kaufanfrage abgebrochen.
